package kr.or.redplus.vo;

public class Res_manageVO {
	private String bdh_id;
	private String day_code;
	private String time_code;
	private String max_user;
	private String cnt_user;
	
	public String getBdh_id() {
		return bdh_id;
	}
	public void setBdh_id(String bdh_id) {
		this.bdh_id = bdh_id;
	}
	public String getDay_code() {
		return day_code;
	}
	public void setDay_code(String day_code) {
		this.day_code = day_code;
	}
	public String getTime_code() {
		return time_code;
	}
	public void setTime_code(String time_code) {
		this.time_code = time_code;
	}
	public String getMax_user() {
		return max_user;
	}
	public void setMax_user(String max_user) {
		this.max_user = max_user;
	}
	public String getCnt_user() {
		return cnt_user;
	}
	public void setCnt_user(String cnt_user) {
		this.cnt_user = cnt_user;
	}

}
